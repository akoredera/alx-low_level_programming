/**
 * op_add - add the values
 * @a: first integer
 * @b: second integer
 * Return: the summation
 */
int op_add(int a, int b)
{
	return ((a) + (b));
}
/**
 * op_sub - return the difference of the values
 * @a: first integer
 * @b: second integer
 * Return: the differences
 */
int op_sub(int a, int b)
{
	return ((a) - (b));
}
/**
 * op_mul - return the product of  the values
 * @a: first integer
 * @b: second integer
 * Return: the product
 */
int op_mul(int a, int b)
{
	return ((a) * (b));
}
/**
 * op_div - returns the result of the division
 * @a: first integer
 * @b: second integer
 * Return: the division
 */
int op_div(int a, int b)
{
	return ((a) / (b));
}
/**
 * op_mod - return the remainder of the division of the values
 * @a: first integer
 * @b: second integer
 * Return: the remainder
 */
int op_mod(int a, int b)
{
	return ((a) % (b));
}
